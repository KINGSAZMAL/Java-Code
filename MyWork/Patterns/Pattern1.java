public class Pattern1 {
    public static void main(String[] args) {
        int i,j;
        int n=5;
        for(i=1;i<=n;i++){
            for(j=1;j<=n;j++){
            System.out.print(i+" ");
        }
        System.out.println();
    }
    System.out.println("----------------------");
    int k,l;
    for(k=1;k<=n;k++){
        for(l=1;l<=n;l++){
            System.out.print(l+" ");
        }
        System.out.println();
    }

    }
}
